#include <stdio.h>

int main() {
    int i;

    // ---------------------------
    // ใช้ while loop
    // ---------------------------
    i = 2; // กำหนดค่าเริ่มต้นของตัวแปร i เป็น 2
    while (i <= 10) { // ตรวจสอบว่า i ยังน้อยกว่าหรือเท่ากับ 10
        printf("%d ", i); // แสดงค่าของ i
        i += 2; // เพิ่มค่า i ทีละ 2 เพื่อให้ได้เลขคู่ถัดไป
    }

    printf("\nEnd of loop (while loop)\n\n");

    // ---------------------------
    // ใช้ for loop
    // ---------------------------
    for (i = 2; i <= 10; i += 2) { // เริ่มที่ 2 และเพิ่มทีละ 2 จนถึง 10
        printf("%d ", i); // แสดงค่าของ i
    }

    printf("\nEnd of loop (for loop)\n");

    return 0; // จบโปรแกรม
}
